export interface ContractVariables {
  share_percentage: number;
  start_date: string;
  end_date: string | null;
}

export function renderArticleText(text: string, vars: ContractVariables): string {
  return text
    .replace(/\{\{share_percentage\}\}/g, String(vars.share_percentage))
    .replace(/\{\{start_date\}\}/g, vars.start_date)
    .replace(/\{\{end_date\}\}/g, vars.end_date || '별도 해지 통보 시까지(무기한)');
}
