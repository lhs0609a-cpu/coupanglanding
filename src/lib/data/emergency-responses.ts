/** 유형 + 서브타입으로 가이드 찾기 */
export function findGuide(incidentType: string, subType: string): EmergencyGuide | undefined {
  return EMERGENCY_GUIDES.find(
    (g) => g.incidentType === incidentType && g.subType === subType,
  );
}

/** 유형별 가이드 목록 */
export function getGuidesByType(incidentType: string): EmergencyGuide[] {
  return EMERGENCY_GUIDES.filter((g) => g.incidentType === incidentType);
}
