// ─── 카테고리 경로 → 그룹ID 매핑 ───────────────────────────

/**
 * categoryPath (쿠팡 경로)에서 CPG 그룹 ID를 찾는다.
 * categoryCode가 있으면 직접 매핑, 없으면 경로 기반 탐색.
 */
export function findGroupId(
  categoryPath: string,
  categoryCode?: string,
): string | null {
  // 1. 카테고리 코드로 직접 매핑
  if (categoryCode && mapping.codeToGroup[categoryCode]) {
    return mapping.codeToGroup[categoryCode];
  }

  // 2. 경로 기반 매핑 — 정확 매칭 또는 가장 긴 프리픽스 매칭
  const normalizedPath = categoryPath.replace(/\s+/g, '');
  const groupKeys = Object.keys(mapping.groups);

  // 정확 매칭
  if (mapping.groups[normalizedPath]) return normalizedPath;

  // 가장 긴 프리픽스 매칭
  let bestMatch: string | null = null;
  let bestLen = 0;

  for (const key of groupKeys) {
    if (normalizedPath.startsWith(key) && key.length > bestLen) {
      bestMatch = key;
      bestLen = key.length;
    }
    // 역방향: 그룹키가 경로의 접두어인 경우
    if (key.startsWith(normalizedPath) && normalizedPath.length > bestLen) {
      bestMatch = key;
      bestLen = normalizedPath.length;
    }
  }

  return bestMatch;
}

// ─── 공개 API ───────────────────────────────────────────────

/**
 * 카테고리 경로 + 코드로 해당 CPG의 콘텐츠 프로필을 반환.
 * 프로필이 없으면 null → 레거시 로직으로 폴백.
 *
 * 우선순위:
 * 1. 코드 레벨 건강식품 오버라이드 (HEALTH_CODE_TO_PROFILE)
 * 2. 그룹 레벨 CPG 프로필 (profileCache)
 * 3. null → 레거시 폴백
 */
export function resolveContentProfile(
  categoryPath: string,
  categoryCode?: string,
): ContentProfile | null {
  // 1. 코드 레벨 건강식품 오버라이드 (카테고리 오염 방지 핵심)
  if (categoryCode && HEALTH_CODE_TO_PROFILE[categoryCode]) {
    const profileKey = HEALTH_CODE_TO_PROFILE[categoryCode];
    const overrideProfile = profileCache.get(profileKey);
    if (overrideProfile) return overrideProfile;
  }

  // 2. 그룹 레벨 CPG 프로필
  const groupId = findGroupId(categoryPath, categoryCode);
  if (!groupId) return null;

  return getProfileSync(groupId);
}

/**
 * 프로필 사전 로딩 (서버 시작 시 또는 첫 요청 시)
 */
export async function initProfiles(): Promise<void> {
  const l1List = Object.keys(L1_FILE_MAP);
  await Promise.all(l1List.map(l1 => loadL1Profiles(l1)));
}

/**
 * 특정 L1 카테고리의 프로필만 로딩
 */
export async function ensureProfilesLoaded(categoryPath: string): Promise<void> {
  const l1 = categoryPath.split('>')[0];
  await loadL1Profiles(l1);
}

