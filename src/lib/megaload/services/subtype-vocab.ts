/**
 * 상품명+경로로 서브타입을 감지해 효과1/성분/추천대상 풀을 좁힌다 (원본 비파괴).
 * 식품(건강식품 성분별)은 별도 디텍터가 처리하므로 여기 SUBTYPE_RULES 에서 제외.
 * @returns 좁혀진 vars 사본. 비대상 L1 이면 원본 그대로.
 */
export function narrowVarsBySubtype(
  vars: Record<string, string[]>,
  categoryPath: string,
  productName?: string,
): Record<string, string[]> {
  const catKey = catKeyFromPath(categoryPath);
  const rules = SUBTYPE_RULES[catKey];
  if (!rules) return vars;
  const lower = `${productName || ''} ${categoryPath}`.toLowerCase();
  let sub: SubVars | undefined;
  for (const [re, sv] of rules) {
    if (re.test(lower)) { sub = sv; break; }
  }
  if (!sub) sub = SUBTYPE_GENERIC[catKey]; // 매칭 실패 → 안전 GENERIC
  if (!sub) return vars;
  const out = { ...vars };
  if (sub.효과1) out['효과1'] = [...sub.효과1];
  if (sub.성분) {
    // ⚠️ 비식품 소재(成分)는 상품당 1개로 고정. 여러 소재가 풀에 있으면
    // 생성기가 문장마다 다른 소재를 뽑아 "TPE 기반…NBR 기술", "극세사…구스…마이크로화이바"
    // 같은 자기모순이 생긴다. 식품은 복수 원료(콘드로이친+글루코사민 등) 자연스러우니 제외.
    if (catKey === '식품') {
      out['성분'] = [...sub.성분];
    } else {
      out['성분'] = [sub.성분[stableHash(`${productName || ''}|성분`) % sub.성분.length]];
    }
  }
  if (sub.추천대상) out['추천대상'] = [...sub.추천대상];
  if (sub.인증) out['인증'] = [...sub.인증];
  // {카테고리} 를 실제 leaf 로 고정 — 형제 상품명(보온도시락/시트커버/카시트/세제 등)이
  // 카테고리 풀에서 무작위로 끼어드는 누출 차단. (식품/뷰티는 SUBTYPE_RULES 없어 미적용)
  const leaf = leafNoun(categoryPath);
  if (leaf && leaf.length >= 2) out['카테고리'] = [leaf];
  return out;
}
