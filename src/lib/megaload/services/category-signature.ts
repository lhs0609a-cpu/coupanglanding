// ============================================================
// 카테고리 매칭 학습용 상품명 시그니처 생성
//
// 목적: 사용자가 수동 수정한 카테고리를 비슷한 상품에 자동 적용하기 위해
//       상품명의 핵심 토큰을 정규화한 시그니처 생성.
//
// 시그니처 동등 = 같은 카테고리로 처리해도 안전한 상품
//
// 정규화 단계:
//   1) 가격/마커/특수문자 제거
//   2) 토큰화 (공백/구두점 split)
//   3) NOISE 토큰 제거 (추천/특가/할인/리뷰 등)
//   4) 길이 ≥ 2 토큰만 유지
//   5) 정렬 (순서 무관 → 동일 시그니처)
//   6) 중복 제거
//   7) 첫 5개 의미 토큰만 유지 (SEO 노이즈 영향 차단)
//   8) "|" 로 join
// ============================================================

const NOISE_TOKENS = new Set([
  // 가격/배송/할인
  '추천','비교','후기','리뷰','할인','특가','무료배송','당일발송','로켓배송','정품','무료','할인가','가격',
  '가성비','만족','베스트','신상','신상품','한정','특별','선물','선물용','선물세트',
  '인기','대박','최고','최저가','최저','득템','쟁여','꿀템','갓성비','명품',
  '정식','한국','한국어','수입','직배송','직수입','직접','전용','구입','쇼핑',
  // 효능 (SEO 스터핑 빈출)
  '안티에이징','보습','주름개선','미백','피부탄력','피부미백','잔주름',
  '체감','효과','즉시','바로','금방','빠른','강력','진한','풍부한','풍성한',
  '인증','검증','승인','자연','순수','자연스러운','진짜','진심','진정한',
  // 단위/수량 (시그니처에서 단위는 의미 약함)
  'kg','g','ml','l','oz','set','pack','box','ea','개','팩','세트','박스',
  // 상품 일반 어휘
  '판매','판매중','베스트셀러','정품인증','국내','해외',
]);

/** 가격/마커 제거 */
function preClean(input: string): string {
  return input
    .replace(/[★☆◆◇■□●○▶▷※♥♡♠♣]/g, ' ')
    .replace(/[\d,]+\s*원/g, ' ')
    .replace(/[\d,]+\s*(?:kg|g|ml|l|oz|개|팩|박스|병|봉|장|마|입)/gi, ' ')
    .replace(/[/()[\]{}<>\\!@#$%^&*\-_=+,.?:;'"`~]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .toLowerCase();
}

/**
 * 상품명 → 정규화된 시그니처 생성.
 * 같은 시그니처는 같은 카테고리로 학습/적용 가능.
 *
 * 빈 문자열 반환 = 시그니처 생성 불가 (학습 미적용)
 */
export function computeSignature(productName: string): string {
  if (!productName) return '';
  const cleaned = preClean(productName);
  if (!cleaned) return '';
  const tokens = cleaned
    .split(/\s+/)
    .filter(t => t.length >= 2 && !NOISE_TOKENS.has(t));
  if (tokens.length === 0) return '';
  // 중복 제거 + 정렬
  const unique = [...new Set(tokens)].sort();
  // 핵심 토큰 5개만 유지 (head 우선)
  const top = unique.slice(0, 5);
  return top.join('|');
}
