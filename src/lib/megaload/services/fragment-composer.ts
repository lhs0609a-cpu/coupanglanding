/**
 * 하나의 ContentBlock을 조각 조합으로 생성한다.
 */
export function composeBlock(
  blockType: ContentBlockType,
  categoryPath: string,
  vars: Record<string, string[]>,
  productName: string,
  seoKeywords: string[],
  rng: () => number,
  forbiddenTerms?: string[],
): ContentBlock {
  // ── v2 완성문 템플릿 뱅크 우선 ──
  // 해당 카테고리에 v2 템플릿이 있으면 조각조합 대신 직접 사용 (자연스러움 보장)
  const v2Block = composeFromV2Templates(
    blockType,
    categoryPath,
    vars,
    productName,
    seoKeywords,
    rng,
    forbiddenTerms,
  );
  if (v2Block) return v2Block;

  // social_proof, usage_guide는 자체 조각풀 사용, 없으면 solution으로 폴백
  const effectiveType = blockType;
  const rawPool = resolveFragments(effectiveType, categoryPath);
  // 안전 기본값 보장
  const pool: FragmentPool = {
    openers: rawPool.openers || [],
    values: rawPool.values || [],
    closers: rawPool.closers || [],
    emphases: rawPool.emphases,
    titles: rawPool.titles,
    item_pool: rawPool.item_pool,
  };
  const hasPool = pool.openers.length > 0 || pool.values.length > 0;

  // 풀이 비어있으면 solution 풀로 폴백 (social_proof, usage_guide 등)
  let actualPool = hasPool ? pool : resolveFragments('solution', categoryPath);

  // ── forbiddenTerms 필터: 프래그먼트 텍스트에서 금지어 포함 항목 제거 ──
  // 프로필 지정값 우선, 없으면 L1 대분류 안전망 적용
  const effectiveForbidden = (forbiddenTerms && forbiddenTerms.length > 0)
    ? forbiddenTerms
    : getL1ForbiddenTerms(categoryPath);
  if (effectiveForbidden.length > 0) {
    actualPool = filterFragmentPool(actualPool, effectiveForbidden);
  }

  // ── 짧은 generic CTA closer 차단 (전수 감사: 1만+회 등장 saturation) ──
  // "확인하세요./경험하세요./쉽고 간편합니다./꾸준함이 답입니다./만나보세요./
  //  간편하게 시작하세요./시작하세요." 등이 모든 카테고리에 박혀 부자연스러움.
  // ⚠️ 주의: 작은 fallback 풀을 자동 주입하면 그것이 새 saturation이 됨 (이전 시도에서 52~109% 등장).
  //   대신 GLOBAL_CTA_CLOSERS(150+ 항목)를 보충 풀로 사용 — 큰 풀이라 saturation 위험 적음.
  const SATURATED_SHORT_CLOSERS = new Set([
    '확인하세요.', '확인해보세요.', '시작하세요.', '경험하세요.', '만나보세요.',
    '쉽고 간편합니다.', '꾸준함이 답입니다.', '간편하게 시작하세요.', '느껴보세요.',
    '바꿔보세요.', '지금 시작하세요.', '지금 만나보세요.', '확인',
  ]);
  if (actualPool.closers && actualPool.closers.length > 0) {
    const filtered = actualPool.closers.filter(c => c && !SATURATED_SHORT_CLOSERS.has(c.trim()));
    if (filtered.length === 0) {
      // 완전히 비면 GLOBAL_CTA_CLOSERS(150+개)에서 시드 기반 12개 — 다양성 보장
      actualPool = { ...actualPool, closers: pickN(filterByTone(GLOBAL_CTA_CLOSERS), 12, rng) };
    } else {
      actualPool = { ...actualPool, closers: filtered };
    }
  }

  // ── 글로벌 다양성 풀 주입 (closer/opener 96만회 반복 문제 해결) ──
  // 시드별로 다른 12개씩 추가되어 카테고리당 풀 크기가 5~10배 확대됨
  // categoryPath를 전달해 식품 카테고리는 식품 전용 풀만 사용 (공산품 톤 차단)
  actualPool = injectGlobalDiversity(actualPool, blockType, rng, categoryPath);
  // forbidden 필터를 한번 더 적용 (글로벌 풀에 카테고리 부적합 단어가 있을 수 있음)
  if (effectiveForbidden.length > 0) {
    actualPool = filterFragmentPool(actualPool, effectiveForbidden);
  }

  switch (blockType) {
    case 'hook':
    case 'solution':
    case 'social_proof':
    case 'usage_guide': {
      // 1차 문장
      const content = composeOneSentence(actualPool, vars, productName, seoKeywords, rng, blockType);
      // 3차 문장 결합 (SEO 권장 2500+자를 위해 분량 확대)
      const sub1 = composeOneSentence(actualPool, vars, productName, seoKeywords, rng, blockType);
      const sub2 = composeOneSentence(actualPool, vars, productName, seoKeywords, rng, blockType);
      const sub3 = composeOneSentence(actualPool, vars, productName, seoKeywords, rng, blockType);
      const seen = new Set([content]);
      const subParts = [];
      for (const s of [sub1, sub2, sub3]) {
        if (s && !seen.has(s)) {
          subParts.push(s);
          seen.add(s);
        }
      }
      const subContent = subParts.join(' ');

      return { type: blockType, content, subContent: subContent || undefined };
    }

    case 'feature_detail': {
      const opener = pickRandom(actualPool.openers, rng);
      const value = pickRandom(actualPool.values, rng);
      const closer = pickRandom(actualPool.closers, rng);
      const emphasisFallback = isFoodCategory(categoryPath) ? FOOD_EMPHASIS_FALLBACK : EMPHASIS_FALLBACK;
      const emphasis = actualPool.emphases && actualPool.emphases.length > 0
        ? actualPool.emphases[Math.floor(rng() * actualPool.emphases.length)]
        : emphasisFallback[Math.floor(rng() * emphasisFallback.length)];

      let rawContent = [opener, value, closer].filter(Boolean).join(' ');
      rawContent = fillTemplate(rawContent, vars, productName, rng);
      rawContent = maybeSeoWeave(rawContent, seoKeywords, rng, blockType);
      rawContent = cleanSpaces(rawContent);

      // subContent — 3 문장 결합 (분량 확대)
      const sub1 = composeOneSentence(actualPool, vars, productName, seoKeywords, rng, blockType);
      const sub2 = composeOneSentence(actualPool, vars, productName, seoKeywords, rng, blockType);
      const sub3 = composeOneSentence(actualPool, vars, productName, seoKeywords, rng, blockType);
      const seen = new Set([rawContent]);
      const subParts = [];
      for (const s of [sub1, sub2, sub3]) {
        if (s && !seen.has(s)) {
          subParts.push(s);
          seen.add(s);
        }
      }
      const subContent = subParts.join(' ');

      const filledEmphasis = emphasis
        ? fillTemplate(emphasis, vars, productName, rng)
        : undefined;

      return {
        type: blockType,
        content: rawContent,
        subContent: subContent || undefined,
        emphasis: filledEmphasis,
      };
    }

    case 'benefits_grid': {
      const title = actualPool.titles && actualPool.titles.length > 0
        ? actualPool.titles[Math.floor(rng() * actualPool.titles.length)]
        : '핵심 장점';

      // item_pool에서 5개 비중복 선택 (기존 3→5)
      const items = selectDistinct(actualPool.item_pool || [], 5, rng);
      const filledItems = items.map(item => {
        let filled = fillTemplate(item, vars, productName, rng);
        // {seo_keyword} 치환
        if (filled.includes('{seo_keyword}') && seoKeywords.length > 0) {
          filled = filled.replace(
            /\{seo_keyword\}/g,
            seoKeywords[Math.floor(rng() * seoKeywords.length)],
          );
        }
        return filled;
      });

      return { type: blockType, content: title, items: filledItems };
    }

    case 'cta': {
      const opener = pickRandom(actualPool.openers, rng);
      const closer = pickRandom(actualPool.closers, rng);

      let rawContent = [opener, closer].filter(Boolean).join(' ');
      rawContent = fillTemplate(rawContent, vars, productName, rng);
      rawContent = maybeSeoWeave(rawContent, seoKeywords, rng, blockType);
      rawContent = cleanSpaces(rawContent);

      // subContent — 2 문장 + 강제 행동 유도 closer
      // — 글의 마지막 200자에 행동 유도 표현이 반드시 들어가도록 보장
      const sub1 = composeOneSentence(actualPool, vars, productName, seoKeywords, rng, blockType);
      const sub2 = composeOneSentence(actualPool, vars, productName, seoKeywords, rng, blockType);
      const forcedCta = CTA_FORCED_CLOSERS[Math.floor(rng() * CTA_FORCED_CLOSERS.length)];
      const seen = new Set([rawContent]);
      const subParts = [];
      for (const s of [sub1, sub2]) {
        if (s && !seen.has(s)) {
          subParts.push(s);
          seen.add(s);
        }
      }
      // 마지막에 강제 CTA closer (체크된 행동 유도 표현)
      subParts.push(forcedCta);
      const subContent = subParts.join(' ');

      return { type: blockType, content: rawContent, subContent: subContent || undefined };
    }

    default: {
      // 미지원 블록타입은 hook 로직으로 폴백
      const fb = resolveFragments('hook', categoryPath);
      const content = composeOneSentence(fb, vars, productName, seoKeywords, rng, blockType);
      const sub1 = composeOneSentence(fb, vars, productName, seoKeywords, rng, blockType);
      const sub2 = composeOneSentence(fb, vars, productName, seoKeywords, rng, blockType);
      const seen = new Set([content]);
      const subParts = [];
      for (const s of [sub1, sub2]) {
        if (s && !seen.has(s)) {
          subParts.push(s);
          seen.add(s);
        }
      }
      return { type: blockType, content, subContent: subParts.join(' ') || undefined };
    }
  }
}

/**
 * 프래그먼트 풀에서 forbiddenTerms 포함 항목을 제거한다.
 * 필터 후 비어있으면 원본 유지 (안전망).
 */
function filterFragmentPool(pool: FragmentPool, forbiddenTerms: string[]): FragmentPool {
  const hasForbidden = (text: string): boolean =>
    forbiddenTerms.some(term => text.includes(term));

  const filterArr = (arr: string[]): string[] => {
    const filtered = arr.filter(s => !hasForbidden(s));
    return filtered.length > 0 ? filtered : arr; // 전부 제거되면 원본 유지
  };

  return {
    openers: filterArr(pool.openers || []),
    values: filterArr(pool.values || []),
    closers: filterArr(pool.closers || []),
    emphases: pool.emphases ? filterArr(pool.emphases) : undefined,
    titles: pool.titles,
    item_pool: pool.item_pool ? filterArr(pool.item_pool) : undefined,
  };
}

/** 하나의 문장을 조각 풀에서 조합한다. */
function composeOneSentence(
  pool: FragmentPool,
  vars: Record<string, string[]>,
  productName: string,
  seoKeywords: string[],
  rng: () => number,
  blockType: string,
): string {
  const opener = pickRandom(pool.openers, rng);
  const value = pickRandom(pool.values, rng);
  const closer = pickRandom(pool.closers, rng);

  let raw = [opener, value, closer].filter(Boolean).join(' ');
  raw = fillTemplate(raw, vars, productName, rng);
  raw = maybeSeoWeave(raw, seoKeywords, rng, blockType);
  raw = cleanSpaces(raw);
  return raw;
}

/** 배열에서 랜덤 1개 선택 (빈 배열이면 '') */
function pickRandom(arr: string[], rng: () => number): string {
  if (!arr || arr.length === 0) return '';
  return arr[Math.floor(rng() * arr.length)];
}

// ─── 프레임워크 전체 블록 시퀀스 조합 ───────────────────

/**
 * 프레임워크의 블록 시퀀스를 조합하여 ContentBlock[] 반환.
 */
export function composeAllBlocks(
  framework: FrameworkDef,
  categoryPath: string,
  vars: Record<string, string[]>,
  productName: string,
  seoKeywords: string[],
  rng: () => number,
  forbiddenTerms?: string[],
): ContentBlock[] {
  resetSeoWeaveCounter();
  // 시드 기반 톤 결정 — 한 글에서 ~다/~요 일관성 유지
  setToneMode(rng() < 0.6 ? 'formal' : 'casual');
  // 블록 시퀀스 보강 — 쿠팡 SEO 권장 2000자+ 충족 위해 신뢰/사용가이드 블록 강제 추가.
  // cta는 항상 마지막에 두어 행동 유도가 글 끝에 위치하도록 보장.
  const baseBlocks = [...framework.blocks];
  const ctaIdx = baseBlocks.lastIndexOf('cta');
  const beforeCta = ctaIdx >= 0 ? baseBlocks.slice(0, ctaIdx) : baseBlocks;
  const ctaPart = ctaIdx >= 0 ? baseBlocks.slice(ctaIdx) : [];
  const enriched = [...beforeCta];
  if (!enriched.includes('social_proof')) enriched.push('social_proof');
  if (!enriched.includes('usage_guide')) enriched.push('usage_guide');
  const finalBlocks = [...enriched, ...ctaPart];
  const rawBlocks = finalBlocks.map(blockType =>
    composeBlock(
      blockType as ContentBlockType,
      categoryPath,
      vars,
      productName,
      seoKeywords,
      rng,
      forbiddenTerms,
    ),
  );

  // ─── Phase A: Cross-paragraph 중복 차단 + 토큰 정규화 + 식품 동사 차환 + 미치환 변수 reject ───
  // (F2/F3/F4/F6 통합 후처리)
  const isFood = isFoodCategory(categoryPath);
  const seenSentences = new Set<string>();

  const splitSentences = (text: string): string[] => {
    if (!text) return [];
    // 한국어 종결 + 영문 종결 분리. 마침표/물음표/느낌표 뒤에 공백 또는 끝.
    return text.split(/(?<=[\.!?])\s+/).map(s => s.trim()).filter(Boolean);
  };

  const cleanText = (text: string): string => {
    if (!text) return text;
    let out = text;
    // 1) 식품이면 동사 차환
    if (isFood) out = applyFoodVerbReplacements(out);
    // 2) 토큰 중복 제거
    out = normalizeRepeatedTokens(out);
    // 3) 문장 단위로 split → 의심 패턴 + 중복 제거
    const sents = splitSentences(out);
    const kept: string[] = [];
    for (const s of sents) {
      if (isSuspiciousFragment(s)) continue;
      // 정규화된 키 (공백/끝점 무시)로 같은 글 안 중복 차단
      const key = s.replace(/[\s\.,!?]+/g, '').replace(/[가-힣A-Za-z0-9]/g, c => c.toLowerCase());
      if (seenSentences.has(key)) continue;
      seenSentences.add(key);
      kept.push(s);
    }
    return kept.join(' ');
  };

  return rawBlocks.map(b => ({
    ...b,
    content: cleanText(b.content),
    subContent: b.subContent ? cleanText(b.subContent) : b.subContent,
    items: b.items?.map(it => {
      const cleaned = cleanText(it);
      return cleaned || it; // 통째로 비우면 안 됨 — 빈 결과 시 원본 유지
    }),
    emphasis: b.emphasis ? cleanText(b.emphasis) : b.emphasis,
  }));
}

