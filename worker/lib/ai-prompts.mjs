function hashSeed(s) {
  let h = 2166136261;
  for (const ch of String(s || 'default')) { h ^= ch.charCodeAt(0); h = Math.imul(h, 16777619); }
  return Math.abs(h);
}
export function pickPersona(seed) { return PERSONAS[hashSeed(seed) % PERSONAS.length]; }

/** 노출상품명/제목 — JSON {displayName, keywords} */
export function buildTitlePrompt(p, persona) {
  // 쿠팡 상위노출 상품명 = 구매자가 검색창에 실제로 치는 명사·스펙의 나열.
  //   구조: [브랜드] [제품명] [핵심스펙(용량·수량·규격)] [속성키워드]
  //   ⚠️ 노출명은 카피가 아니다 — 페르소나 톤/형용사를 넣으면 검색 매칭이 깨진다.
  const system = `당신은 쿠팡 검색 상위노출(SEO) 상품명 생성기다. 광고 카피가 아니라, 구매자가 검색창에 실제로 입력하는 "명사 + 스펙 + 다양한 검색 속성어" 조합을 만든다.
${FORBIDDEN_RULE}

[상품명 구조] 아래 순서로 띄어쓰기로만 나열한다(문장 아님):
  제품 핵심명 → 핵심스펙(용량/수량/규격/맛/종류/원산지/재질) → 검색 속성 키워드 3~5개(용도·대상·형태·특성 등)
[규칙]
- ⚠️ 브랜드명은 절대 넣지 말 것. 브랜드는 별도 필드로 등록되므로 노출명에서는 뺀다(예: "현대농산 발아현미"→"발아현미", "에코서트 알로에"→"알로에"). 상호·상표·브랜드로 보이는 고유명(회사명·스토어명)은 제외한다.
- 길이: 공백 포함 35~60자 권장(최대 90자). 짧게 자르지 말고, 실제 검색되는 속성어를 풍부하게 붙여 길고 다양하게.
- 원본 상품명에 있는 실제 스펙(용량 750ml, 수량 24개, 원산지 국산 등)은 반드시 보존한다(지어내지 말 것).
- 검색 속성어를 다양하게: 용도(요리용·선물용·간식용), 대상(성인·아이·반려), 형태(대용량·개별포장·낱개), 동의어(다진마늘=깐마늘) 등 서로 다른 각도의 명사를 3~5개 덧붙인다(같은 뜻 반복 금지).
- 절대 넣지 말 것(검색 노이즈·과장): 합리적/최고/프리미엄/인기/추천/특가/득템/강추/알뜰/필수템 같은 주관·홍보 형용사, "세트/선물세트"(원본에 실제로 세트 구성일 때만), 느낌표·이모지·특수기호, 같은 단어 반복.
- 조사/서술어(입니다·좋은·완벽한 등) 금지 — 명사구로만.

출력은 JSON만: {"displayName": "...", "keywords": ["...5개..."]}. keywords 는 상품명에 다 못 넣은 추가 검색어(동의어·용도·대상) 5개.`;
  const prompt = `원본 상품명: ${p.originalName}
카테고리: ${p.categoryPath || '미상'}
브랜드(노출명에 넣지 말 것): ${p.brand || '없음'}
핵심 특징(스펙): ${(p.features || []).join(', ') || '미상'}

[좋은 예 — 브랜드 제외, 길고 다양하게]
- 원본 "리무스 모스카토 무알콜 스파클링 와인 750ml 달콤한" → {"displayName":"모스카토 무알콜 스파클링 와인 750ml 논알콜 달달한 파티 선물용","keywords":["논알콜와인","모스카토","달달한와인","선물용와인","파티와인"]}
- 원본 "곰곰 국내산 깐마늘 1kg 아삭한 요리용" → {"displayName":"국내산 깐마늘 1kg 다진마늘 요리용 김장 반찬 대용량","keywords":["깐마늘","국산마늘","다진마늘","김장마늘","마늘1kg"]}
- 원본 "현대농산 국산 발아현미 20곡 혼합곡 2kg" → {"displayName":"국산 발아현미 20곡 혼합곡 2kg 잡곡밥 건강 잡곡 대용량","keywords":["발아현미","혼합곡","잡곡","20곡","현미밥"]}
→ 위 상품의 노출상품명(displayName)과 검색 키워드 5개를 JSON으로. 브랜드명 없이, 제품명+스펙+다양한 속성 검색어로 길고 풍부하게.`;
  return { system, prompt, format: 'json', options: { temperature: 0.4, num_predict: 260 } };
}

/** 카테고리 — 후보가 있으면 그 중 선택, 없으면 경로 추론. JSON {categoryPath, confidence} */
export function buildCategoryPrompt(p, candidates = []) {
  const hasCand = candidates.length > 0;
  // 소싱 원본 카테고리(네이버 등)는 이미 사람이 분류한 near-ground-truth → 앵커로 준다.
  const srcCat = String(p.categoryPath || '').trim();
  const system = `당신은 쿠팡 카테고리 분류기다. 상품명 글자만 보지 말고 "원본 카테고리·브랜드·특징"으로 상품이 실제로 무엇인지 이해한 뒤 고른다.
⚠️ 핵심 규칙: **소싱 원본 카테고리와 의미가 일치하는 후보**를 고른다. 원본이 '맥주/음료'면 절대 '도서/가구/완구'를 고르지 않는다(상품명에 우연히 든 브랜드어 '클라우드'·'허브'·'와인' 등에 속지 말 것 — 그건 책·가구가 아니다).
출력은 JSON만: {"categoryPath": "대>중>소>세부", "confidence": 0~1}.`;
  const ctxLines = [
    `상품명: ${p.originalName}`,
    srcCat ? `원본 카테고리(소싱처 분류 — 이것과 의미가 맞는 후보를 최우선): ${srcCat}` : '',
    p.brand ? `브랜드: ${p.brand}` : '',
    `특징: ${(p.features || []).join(', ') || '미상'}`,
  ].filter(Boolean).join('\n');
  const prompt = hasCand
    ? `${ctxLines}\n\n후보 카테고리(아래 문자열 중 하나를 글자 그대로 복사):\n${candidates.map((c, i) => `${i + 1}. ${c}`).join('\n')}\n→ 원본 카테고리와 의미가 가장 일치하는 1개의 categoryPath 를 후보 문자열 그대로(변형·한자추가 금지) JSON으로. 확신이 낮으면 confidence 를 낮게.`
    : `${ctxLines}\n→ 쿠팡식 카테고리 경로(대>중>소>세부)를 추론해 JSON으로. 한자 금지, 순한국어.`;
  return { system, prompt, format: 'json', options: { temperature: 0.2, num_predict: 120 } };
}

/** 옵션 — JSON {options:[{name,value,unit?}]}. 상품명/특징에서 도출 가능한 것만(환각 금지) */
export function buildOptionsPrompt(p) {
  const system = `당신은 쿠팡 상품 옵션 추출기다. 출력은 JSON만: {"options":[{"name":"옵션명","value":"옵션값","unit":"단위(없으면 생략)"}]}.
규칙: 상품명/특징에 실제로 드러난 정보(용량·수량·색상·사이즈·맛/종류 등)만 옵션으로. 없는 스펙은 절대 지어내지 말 것. 1~4개. 한자 금지.`;
  const prompt = `상품명: ${p.originalName}
특징: ${(p.features || []).join(', ') || '없음'}
→ 위에서 확인되는 구매옵션만 JSON으로. (예: 용량 50ml, 수량 120정, 색상 베이지)`;
  return { system, prompt, format: 'json', options: { temperature: 0.1, num_predict: 200 } };
}

/** 상세페이지(스토리) — 섹션형 한국어 카피. 일반 텍스트(마크다운 허용).
 *  p: { originalName, categoryPath, features[], leaf?, seoKeywords?[] }
 *  fixNote: 재시도 시 직전 출력의 문제를 교정 지시로 주입(검증 실패 피드백). */
export function buildDetailPrompt(p, persona, { maxTokens = 1100, fixNote = '' } = {}) {
  const leaf = (p.leaf || (p.categoryPath || '').split('>').pop() || p.originalName || '').trim();
  const seo = (p.seoKeywords || []).filter(Boolean).slice(0, 8);
  const system = `당신은 쿠팡 1페이지 상위노출과 구매전환을 동시에 잡는 한국어 상세페이지 카피라이터다. ${persona.style}.

[작성 대상] 오직 "${leaf}"(${p.categoryPath || leaf}) 상품 하나만 다룬다. 다른 종류의 상품이나 다른 카테고리의 기능·부품·효능(예: 시계인데 매트리스·수면, 마우스인데 공기청정·난방)을 절대 언급하지 않는다. 모든 문장이 "${leaf}"에 정확히 들어맞아야 한다.

[이 상품만의 특징 — 매우 중요] 카테고리 일반론("그냥 바디로션이라 촉촉해요")에 머물지 마라. 상품명 "${p.originalName}"과 특징에 담긴 **이 상품만의 차별점**(핵심 성분·인증·함량/농도·용량·색상·향·기능 키워드 등, 예: "알로에 96%", "세라마이드", "에코서트 인증", "무알콜", "500ml 대용량")을 찾아, 본문에서 구체적으로 이름을 불러 이야기한다. 같은 카테고리의 다른 제품과 무엇이 다른지가 드러나야 한다. (단, 상품명·특징에 근거 없는 성분·효능·인증을 지어내지는 말 것.)

[톤 — 가장 중요] 광고 같지 않게, 실제로 써본 사람이 솔직하게 추천하듯 자연스러운 후기 말투로 쓴다. 공감되는 일상 장면("이런 적 있으시죠?", "막상 받아보면")으로 시작해, 생생한 사용 경험과 이득을 이야기하듯 풀어낸다. 읽다 보면 "이거 지금 사야겠다" 싶게 구매욕이 차오르도록. (단, 특정 구매자를 사칭한 거짓 후기 — "제가 3개월 써보니" 같은 단정 — 는 쓰지 말고, 누구나 공감할 장면과 솔직한 추천 톤으로.)

[흐름]
1) 공감 후킹 한 문장(굵게 **...**) — 구매자가 겪는 상황/고민을 콕 집는다.
2) 자연스러운 본문 — "${leaf}"가 그 고민을 어떻게 풀어주는지 후기처럼 생생하게(문단 2~3개). 핵심 장점은 '- ' 불릿 3~4개로 정리하되 각 불릿도 딱딱한 스펙이 아니라 "그래서 뭐가 좋은지" 체감 이득 위주로.
3) 어떤 사람·어떤 순간에 특히 좋은지 1~2문장.
4) 부드럽지만 강하게 구매를 권하는 마무리 한 문장.

[쿠팡 SEO]
- 분량: 공백 제외 약 600~1200자(너무 짧으면 노출·신뢰 약함, 너무 길면 이탈).
- 키워드: 다음을 본문에 자연스럽게 녹인다(어색한 나열 금지): ${seo.join(', ') || leaf}. 특히 "${leaf}"는 본문 전체에서 2~4회 자연스럽게 반복 노출.

[문체] 순한국어 존댓말. 한자(漢字)·중국어·일본어·영어 문장 금지(USB·ml·LED 같은 표준 단위/약어만 허용). 같은 문장·문구 반복 금지.

[금지] ${FORBIDDEN_RULE}

[출력] 완성된 카피 본문만. '헤드라인','불릿','추천 대상','(1)','상세페이지 본문' 같은 지시문·라벨·번호를 출력에 쓰지 않는다.${fixNote ? `\n\n[직전 출력 문제 — 반드시 교정] ${fixNote}` : ''}`;
  const prompt = `상품명: ${p.originalName}
카테고리: ${p.categoryPath || '미상'}
핵심 특징: ${(p.features || []).join(', ') || '미상'}
강조점: ${persona.focus}
위 "${leaf}" 상품의 쿠팡 상세페이지 본문을, 자연스러운 후기 말투로 구매욕이 폭발하게, SEO 분량·키워드를 맞춰 작성해줘.`;
  return { system, prompt, options: { temperature: 0.8, num_predict: maxTokens } };
}
