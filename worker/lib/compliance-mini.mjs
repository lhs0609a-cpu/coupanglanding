/**
 * 경량 규제 금지어 가드 (워커/오프라인용)
 * ---------------------------------------------------------------------------
 * LLM 자유 생성 결과를 1차로 걸러 재생성 루프에 쓴다. 표시광고법/건기식법/
 * 화장품법/약사법의 '확실한 위반(error)' 핵심만 담는다.
 *
 * ⚠️ 이건 1차 가드일 뿐, 최종 대량등록 직전에는 웹의 풀 compliance-filter
 *    (src/lib/megaload/services/compliance-filter.ts)로 반드시 한 번 더 게이트한다.
 */

// 카테고리 맥락상 허용되는 경우(생활용품 세제의 '살균/항균' 등)
const HOUSEHOLD = ['세제', '세탁', '세정', '살균제', '소독', '청소', '주방', '욕실', '화장실'];
const isHousehold = (ctx = '') => HOUSEHOLD.some((k) => String(ctx).includes(k));

/** [정규식, 라벨] — 확실한 위반(error)만 */
const ERROR_TERMS = [
  // 건기식법/약사법 — 질병 치료·예방·의약품 오인
  [/치료/g, '치료'], [/완치/g, '완치'], [/항암/g, '항암'], [/항염/g, '항염'],
  [/고혈압/g, '고혈압'], [/당뇨(?![식기계])/g, '당뇨'], [/치매/g, '치매'],
  [/골다공증/g, '골다공증'], [/아토피/g, '아토피'], [/관절염/g, '관절염'],
  [/해독/g, '해독'], [/디톡스/g, '디톡스'], [/만병통치/g, '만병통치'],
  [/약효/g, '약효'], [/진통/g, '진통'], [/소염/g, '소염'], [/질병\s*예방/g, '질병예방'],
  [/면역력\s*(증진|강화|향상)/g, '면역력 증진'], [/혈관\s*청소/g, '혈관청소'],
  // 탈모/발모·모발성장 — 건기식/화장품 의약품 오인(자주 흘리는 효능 단정)
  [/탈모/g, '탈모'], [/발모/g, '발모'], [/모발\s*(성장|재생|촉진)/g, '모발 성장'],
  [/(머리카락|모발|숱)[가-힣\s]{0,6}(빠지는\s*양|빠짐)[가-힣\s]{0,6}(줄|감소|방지|막)/g, '탈모 개선'],
  [/(머리카락|모발)[가-힣\s]{0,8}(다시\s*(자라|나)|자라기\s*시작|나기\s*시작)/g, '발모(재생) 단정'],
  [/(숱|머리숱)[가-힣\s]{0,6}(늘|많아|풍성)/g, '모발 증가 단정'],
  [/성장\s*속도[가-힣\s]{0,6}(빨라|증가|촉진)/g, '성장 촉진'],
  [/부작용\s*(이?\s*)?없(는|음|다)/g, '부작용 없음'], [/의약품/g, '의약품'],
  [/(질환|질병)\s*(개선|완화|치유)/g, '질환 개선'],
  // 화장품법 — 의학적 효능·기능성 오인
  [/(피부)?\s*재생/g, '재생'], [/세포\s*(재생|활성)/g, '세포재생'],
  [/노화\s*방지/g, '노화방지'], [/항노화/g, '항노화'], [/안티에이징(?!.*화장품)/g, '안티에이징'],
  [/모공\s*(축소|제거|수축)/g, '모공축소/제거'], [/여드름\s*(치료|제거)/g, '여드름 치료'],
  [/주름\s*(제거|없애|개선)/g, '주름 제거/개선'], [/미백/g, '미백(기능성)'],
  [/탄력\s*(강화|개선|증진)/g, '탄력 강화/개선'], [/(피부)?\s*리프팅/g, '리프팅'],
  // 환각성 인증/시험 — 사실 아니면 위반 (1차 경고용)
  [/FDA\s*인증/gi, 'FDA 인증'], [/임상\s*(시험|실험)\s*(완료|통과)/g, '임상시험 완료'],
  [/특허\s*(받은|등록)(?!.*[0-9])/g, '특허(근거 불명)'],
  // 표시광고법 — 절대적/최상급·객관적 근거 없는 단정
  [/100\s*%\s*(효과|보장|완벽)/g, '100% 효과/보장'],
  [/(국내|업계|세계)\s*(최고|1위|유일|최초)(?!의?\s*(브랜드명|상표))/g, '최상급 단정'],
  [/(완벽|완전)\s*(보장|해결)/g, '완벽 보장'],
  [/즉시\s*효과/g, '즉시 효과'], [/영구\s*(제거|효과)/g, '영구 효과'],
];

/**
 * 위반 검사.
 * @param {string} text
 * @param {string} [categoryContext]  카테고리 경로(맥락 허용 판단용)
 * @returns {{ ok: boolean, violations: string[] }}
 */
export function checkMini(text, categoryContext = '') {
  if (!text) return { ok: true, violations: [] };
  const household = isHousehold(categoryContext);
  const found = new Set();
  for (const [re, label] of ERROR_TERMS) {
    re.lastIndex = 0;
    if (re.test(text)) {
      // 생활용품 세제 맥락에서 '살균/항균'류는 허용 — 해당 라벨만 예외 (현재 목록엔 없음, 확장 대비)
      if (household && /살균|항균|소독/.test(label)) continue;
      found.add(label);
    }
  }
  return { ok: found.size === 0, violations: [...found] };
}
