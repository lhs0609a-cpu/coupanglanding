-- 1. CS 카테고리 (분류 체계)
CREATE TABLE IF NOT EXISTS sh_cs_categories (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name TEXT NOT NULL,
  icon TEXT,
  color TEXT,
  priority INT NOT NULL DEFAULT 0,
  is_active BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- 2. 키워드 매칭 규칙
CREATE TABLE IF NOT EXISTS sh_cs_keyword_rules (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  category_id UUID NOT NULL REFERENCES sh_cs_categories(id) ON DELETE CASCADE,
  keywords TEXT[] NOT NULL,
  match_mode TEXT DEFAULT 'any',
  is_active BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- 3. 기존 sh_cs_templates 확장
ALTER TABLE sh_cs_templates
  ADD COLUMN IF NOT EXISTS category_id UUID REFERENCES sh_cs_categories(id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS order_status_condition TEXT,
  ADD COLUMN IF NOT EXISTS variables TEXT[] DEFAULT '{}',
  ADD COLUMN IF NOT EXISTS sort_order INT DEFAULT 0,
  ADD COLUMN IF NOT EXISTS use_count INT DEFAULT 0,
  ADD COLUMN IF NOT EXISTS is_default BOOLEAN DEFAULT false;

-- 4. sh_cs_inquiries 확장
ALTER TABLE sh_cs_inquiries
  ADD COLUMN IF NOT EXISTS category_id UUID REFERENCES sh_cs_categories(id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS urgency TEXT DEFAULT 'normal',
  ADD COLUMN IF NOT EXISTS inquiry_source TEXT DEFAULT 'product',
  ADD COLUMN IF NOT EXISTS channel_order_id TEXT,
  ADD COLUMN IF NOT EXISTS channel_product_name TEXT,
  ADD COLUMN IF NOT EXISTS template_id UUID REFERENCES sh_cs_templates(id) ON DELETE SET NULL;

-- 인덱스
CREATE INDEX IF NOT EXISTS idx_cs_keyword_rules_cat ON sh_cs_keyword_rules(category_id);
CREATE INDEX IF NOT EXISTS idx_cs_templates_cat ON sh_cs_templates(category_id);
CREATE INDEX IF NOT EXISTS idx_cs_inquiries_cat ON sh_cs_inquiries(category_id);
CREATE INDEX IF NOT EXISTS idx_cs_inquiries_urgency ON sh_cs_inquiries(urgency);

