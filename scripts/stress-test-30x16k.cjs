for (const [code, v] of allCats) {
  const p = v.p;
  const expected = expectedByPath(p);
  const productNames = generateProductNames(p, 30);

  for (const name of productNames) {
    totalTested++;
    let hint;
    if (HINT_MODE === 'name-only') hint = name;
    else if (HINT_MODE === 'path-only') hint = p;
    else hint = p; // production: categoryPath 우선 (모든 호출부 통일)

    const { best } = predict(hint);
    if (best === expected) {
      totalPassed++;
    } else {
      totalFailed++;
      if (failures.length < 100) {
        failures.push({ code, path: p, name, expected, actual: best });
      }
      failureGroupsByCategory.set(p, (failureGroupsByCategory.get(p) || 0) + 1);
      const fk = expected + ' → ' + best;
      failureGroupsByPredicted.set(fk, (failureGroupsByPredicted.get(fk) || 0) + 1);
    }
  }
}

