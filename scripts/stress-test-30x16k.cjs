function scoreNoticeCategory(noticeCategoryName, hint) {
  if (!hint || !noticeCategoryName) return 0;
  const pathL = hint.toLowerCase();
  const name = noticeCategoryName.toLowerCase();
  let score = 0;
  let matched = false;
  for (const rule of NOTICE_CATEGORY_RULES) {
    if (!rule.pathRegex.test(pathL)) continue;
    matched = true;
    for (const kw of rule.expect) if (name.includes(kw.toLowerCase())) score += 20;
    if (rule.avoid) for (const kw of rule.avoid) if (name.includes(kw.toLowerCase())) score -= 30;
    break;
  }
  if (!matched) {
    for (const rule of PRODUCT_NAME_TOKEN_RULES) {
      if (!rule.tokenRegex.test(pathL)) continue;
      for (const kw of rule.expect) if (name.includes(kw.toLowerCase())) score += 20;
      if (rule.avoid) for (const kw of rule.avoid) if (name.includes(kw.toLowerCase())) score -= 30;
      break;
    }
  }
  if (/기타/.test(name) && score < 20) score -= 3;
  return score;
}

const STD = [
  '의류', '구두/신발', '가방', '패션잡화', '시계', '쥬얼리/액세서리',
  '화장품 및 인체적용제품',
  '가공식품', '건강기능식품', '농산물', '축산물', '수산물',
  '영상가전제품', '가전제품', '디지털기기',
  '가구', '침구/커튼/카페트류',
  '의약외품', '의료기기', '생활화학제품',
  '자동차용품', '사무/문구용품', '완구용품', '도서/음반/영상저작물',
  '스포츠/레저용품', '욕실용품', '주방용품', '기타 재화',
];
function predict(hint) {
  let best = null, bestScore = -Infinity;
  for (const c of STD) {
    const s = scoreNoticeCategory(c, hint);
    if (s > bestScore) { bestScore = s; best = c; }
  }
  return { best, score: bestScore };
}

// 카테고리 leaf 기반으로 30개 변형 상품명 생성
function generateProductNames(categoryPath, count = 30) {
  const parts = categoryPath.split('>');
  const leaf = parts[parts.length - 1].replace(/[/(),]/g, ' ').trim();
  const l3 = parts[parts.length - 2] || '';
  const l2 = parts[1] || '';

  const sizes = ['', ' 50ml', ' 100ml', ' 200ml', ' 500ml', ' 1L', ' 100g', ' 500g', ' 1kg'];
  const counts = ['', ' 30정', ' 60캡슐', ' 90정', ' 5개입', ' 10개입', ' 30매', ' 100매', ' 12P'];
  const brands = ['', '프리미엄 ', '베스트 ', '인기 ', '국내산 ', '대용량 ', '소용량 '];
  const qualifiers = ['', ' 추천', ' 무료배송', ' 당일출고', ' 정품', ' 신상', ' 1+1', ' 핸드메이드'];

  const names = new Set();
  // 베이스: leaf 자체
  names.add(leaf);
  // L3 + leaf
  if (l3 && l3 !== leaf) names.add(l3 + ' ' + leaf);
  // brand × size
  for (const b of brands) for (const s of sizes) {
    if (names.size >= count) break;
    names.add((b + leaf + s).trim());
  }
  // brand × counts
  for (const b of brands) for (const c of counts) {
    if (names.size >= count) break;
    names.add((b + leaf + c).trim());
  }
  // qualifiers
  for (const q of qualifiers) {
    if (names.size >= count) break;
    names.add((leaf + q).trim());
  }
  return [...names].slice(0, count);
}

