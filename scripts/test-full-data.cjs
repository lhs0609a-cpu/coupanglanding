function pick(arr) { return arr[Math.floor(Math.random() * arr.length)]; }
function pickN(arr, n) {
  const shuffled = [...arr].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
}

// 상품명 생성 함수 — 키워드 배열에서 하나를 골라 브랜드/옵션과 조합
function generateProducts(keywords, count, extraParts) {
  const results = new Set();
  let attempts = 0;
  while (results.size < count && attempts < count * 10) {
    attempts++;
    const kw = pick(keywords);
    const brand = pick(BRANDS);
    const qty = pick(QUANTITIES);
    const opt = pick(OPTIONS);
    const adj = pick(ADJECTIVES);
    const extra = extraParts ? pick(extraParts) : '';

    // 다양한 패턴으로 상품명 생성
    const patterns = [
      `${brand} ${kw} ${qty} ${opt}`,
      `${brand} ${adj} ${kw} ${qty}`,
      `${kw} ${adj} ${qty} ${opt}`,
      `${brand} ${kw} ${extra} ${qty}`.trim(),
      `${adj} ${kw} ${qty} ${opt}`,
      `${kw} ${qty} ${opt}`,
      `${brand} ${kw} ${extra} ${qty} ${opt}`.trim(),
    ];
    results.add(pick(patterns));
  }
  return [...results].slice(0, count);
}

